namespace SearchEngine;

/// <summary>
/// Содержит значения по умолчанию для построения поискового индекса.
/// </summary>
internal static class IndexBuildDefaults
{
  /// <summary>
  /// Минимальный размер источника, начиная с которого автоматически допускается
  /// параллельное построение индекса.
  /// </summary>
  /// <remarks>
  /// Значение выбрано консервативно: на небольших и средних наборах данных
  /// параллельная индексация заметно увеличивает давление на память и не даёт
  /// устойчивого выигрыша по времени. При необходимости пользователь может
  /// включить параллельную обработку явно через <c>forceParallel</c>.
  /// </remarks>
  internal const int ParallelProcessingThreshold = 250_000;
}