namespace SearchEngine;

/// <summary>
/// Определяет, как объединяются результаты по словам поискового запроса.
/// </summary>
public enum QueryMatchMode
{
  /// <summary>
  /// В результате должны присутствовать совпадения по всем словам запроса.
  /// </summary>
  AllTerms,

  /// <summary>
  /// В результате достаточно совпадения хотя бы по одному слову запроса.
  /// </summary>
  AnyTerm,

  /// <summary>
  /// Предпочтительно совпадение по всем словам, но допускается ослабленное объединение результатов.
  /// </summary>
  SoftAllTerms
}
